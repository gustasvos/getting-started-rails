class ArticlesController < ApplicationController
  # each method must have a view to render.
  # def index method will render 'app/views/articles/index.html.erb' and so on.

  def index
    @articles = Article.all
  end

  def show
    @article = Article.find(params[:id])
  end

  def new
    @article = Article.new
  end

  def create
    @article = Article.new(title: "...", body: "...")

    if @article.save
      redirect_to @article
    else
      render :new, status: :unprocessable_entity
    end
  end
end